package model;

public class EstadoModel {
	Integer IDEstado;
	String Nombre;
	
	public EstadoModel() {
		
	}

	public Integer getIDEstado() {
		return IDEstado;
	}

	public void setIDEstado(Integer iDEstado) {
		IDEstado = iDEstado;
	}

	public String getNombre() {
		return Nombre;
	}

	public void setNombre(String nombre) {
		Nombre = nombre;
	}
	
}
